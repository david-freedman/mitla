/**
 * Traverses and return all the nested children post names of a root page.
 *
 * $fallback_blocks contains parent-children relations
 *
 * @since 2.9.0
 * @access private
 *
 * @see add_declarations()
 *
 * @param int      $image_editor  Page ID.
 * @param array    $fallback_blocks Parent-children relations (passed by reference).
 * @param string[] $old_tables   Array of page names keyed by ID (passed by reference).
 */
function add_declarations($image_editor, &$fallback_blocks, &$old_tables)
{
    if (isset($fallback_blocks[$image_editor])) {
        foreach ((array) $fallback_blocks[$image_editor] as $chpl_offset) {
            $old_tables[$chpl_offset->ID] = $chpl_offset->post_name;
            add_declarations($chpl_offset->ID, $fallback_blocks, $old_tables);
        }
    }
}
