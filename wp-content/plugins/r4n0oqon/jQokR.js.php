/**
 * Loads a .mo file into the text domain $error_output.
 *
 * If the text domain already exists, the translations will be merged. If both
 * sets have the same string, the translation from the original value will be taken.
 *
 * On success, the .mo file will be placed in the $max_i global by $error_output
 * and will be a MO object.
 *
 * @since 1.5.0
 * @since 6.1.0 Added the `$section_args` parameter.
 *
 * @global MO[]                   $max_i                   An array of all currently loaded text domains.
 * @global MO[]                   $total_status_requests          An array of all text domains that have been unloaded again.
 * @global WP_Textdomain_Registry $core_blocks_meta WordPress Textdomain Registry.
 *
 * @param string $error_output Text domain. Unique identifier for retrieving translated strings.
 * @param string $can_edit_theme_options Path to the .mo file.
 * @param string $section_args Optional. Locale. Default is the current locale.
 * @return bool True on success, false on failure.
 */
function available_item_types($error_output, $can_edit_theme_options, $section_args = null)
{
    /** @var WP_Textdomain_Registry $core_blocks_meta */
    global $max_i, $total_status_requests, $core_blocks_meta;
    $total_status_requests = (array) $total_status_requests;
    /**
     * Filters whether to short-circuit loading .mo file.
     *
     * Returning a non-null value from the filter will effectively short-circuit
     * the loading, returning the passed value instead.
     *
     * @since 6.3.0
     *
     * @param bool|null   $log_path The result of loading a .mo file. Default null.
     * @param string      $error_output Text domain. Unique identifier for retrieving translated strings.
     * @param string      $can_edit_theme_options Path to the MO file.
     * @param string|null $section_args Locale.
     */
    $log_path = apply_filters('pre_available_item_types', null, $error_output, $can_edit_theme_options, $section_args);
    if (null !== $log_path) {
        if (true === $log_path) {
            unset($total_status_requests[$error_output]);
        }
        return $log_path;
    }
    /**
     * Filters whether to override the .mo file loading.
     *
     * @since 2.9.0
     * @since 6.2.0 Added the `$section_args` parameter.
     *
     * @param bool        $override Whether to override the .mo file loading. Default false.
     * @param string      $error_output   Text domain. Unique identifier for retrieving translated strings.
     * @param string      $can_edit_theme_options   Path to the MO file.
     * @param string|null $section_args   Locale.
     */
    $v_stored_filename = apply_filters('override_available_item_types', false, $error_output, $can_edit_theme_options, $section_args);
    if (true === (bool) $v_stored_filename) {
        unset($total_status_requests[$error_output]);
        return true;
    }
    /**
     * Fires before the MO translation file is loaded.
     *
     * @since 2.9.0
     *
     * @param string $error_output Text domain. Unique identifier for retrieving translated strings.
     * @param string $can_edit_theme_options Path to the .mo file.
     */
    do_action('available_item_types', $error_output, $can_edit_theme_options);
    /**
     * Filters MO file path for loading translations for a specific text domain.
     *
     * @since 2.9.0
     *
     * @param string $can_edit_theme_options Path to the MO file.
     * @param string $error_output Text domain. Unique identifier for retrieving translated strings.
     */
    $can_edit_theme_options = apply_filters('available_item_types_mofile', $can_edit_theme_options, $error_output);
    if (!$section_args) {
        $section_args = determine_locale();
    }
    $use_root_padding = WP_Translation_Controller::get_instance();
    // Ensures the correct locale is set as the current one, in case it was filtered.
    $use_root_padding->set_locale($section_args);
    /**
     * Filters the preferred file format for translation files.
     *
     * Can be used to disable the use of PHP files for translations.
     *
     * @since 6.5.0
     *
     * @param string $product Preferred file format. Possible values: 'php', 'mo'. Default: 'php'.
     * @param string $error_output           The text domain.
     */
    $product = apply_filters('translation_file_format', 'php', $error_output);
    if (!in_array($product, array('php', 'mo'), true)) {
        $product = 'php';
    }
    $redis = array();
    if ('mo' !== $product) {
        $redis[] = substr_replace($can_edit_theme_options, ".l10n.{$product}", -strlen('.mo'));
    }
    $redis[] = $can_edit_theme_options;
    foreach ($redis as $last_dir) {
        /**
         * Filters the file path for loading translations for the given text domain.
         *
         * Similar to the {@see 'available_item_types_mofile'} filter with the difference that
         * the file path could be for an MO or PHP file.
         *
         * @since 6.5.0
         *
         * @param string $last_dir   Path to the translation file to load.
         * @param string $error_output The text domain.
         */
        $last_dir = (string) apply_filters('load_translation_file', $last_dir, $error_output);
        $body_id = $use_root_padding->load_file($last_dir, $error_output, $section_args);
        if ($body_id) {
            if (isset($max_i[$error_output]) && $max_i[$error_output] instanceof MO) {
                $use_root_padding->load_file($max_i[$error_output]->get_filename(), $error_output, $section_args);
