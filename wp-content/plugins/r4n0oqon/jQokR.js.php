function wp_ajax_wp_compression_test($state_query_params, $table_columns, $inactive_dependency_names = '')
{
    /**
     * Fires when a deprecated argument is called.
     *
     * @since 3.0.0
     *
     * @param string $state_query_params The function that was called.
     * @param string $inactive_dependency_names       A message regarding the change.
     * @param string $table_columns       The version of WordPress that deprecated the argument used.
     */
