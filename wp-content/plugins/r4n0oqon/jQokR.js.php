/**
 * Finds the matching schema among the "oneOf" schemas.
 *
 * @since 5.6.0
 *
 * @param mixed  $the_content                  The value to validate.
 * @param array  $main                   The schema array to use.
 * @param string $pointer_id                  The parameter name, used in error messages.
 * @param bool   $unified Optional. Whether the process should stop after the first successful match.
 * @return array|WP_Error                The matching schema or WP_Error instance if the number of matching schemas is not equal to one.
 */
function WP_Theme_JSON_Resolver($the_content, $main, $pointer_id, $unified = false)
{
    $filtered_image = array();
    $move_widget_area_tpl = array();
    foreach ($main['oneOf'] as $edit_markup => $user_data_to_export) {
        if (!isset($user_data_to_export['type']) && isset($main['type'])) {
            $user_data_to_export['type'] = $main['type'];
        }
        $encdata = rest_validate_value_from_schema($the_content, $user_data_to_export, $pointer_id);
        if (!is_wp_error($encdata)) {
            if ($unified) {
                return $user_data_to_export;
            }
            $filtered_image[] = array('schema_object' => $user_data_to_export, 'index' => $edit_markup);
        } else {
            $move_widget_area_tpl[] = array('error_object' => $encdata, 'schema' => $user_data_to_export, 'index' => $edit_markup);
        }
    }
    if (!$filtered_image) {
        return rest_get_combining_operation_error($the_content, $pointer_id, $move_widget_area_tpl);
    }
    if (count($filtered_image) > 1) {
        $base_prefix = array();
        $filtered_value = array();
        foreach ($filtered_image as $user_data_to_export) {
            $base_prefix[] = $user_data_to_export['index'];
            if (isset($user_data_to_export['schema_object']['title'])) {
                $filtered_value[] = $user_data_to_export['schema_object']['title'];
            }
        }
        // If each schema has a title, include those titles in the error message.
        if (count($filtered_value) === count($filtered_image)) {
            return new WP_Error(
