 * to get the backtrace up to what file and function used the deprecated argument.
 *
 * The current behavior is to trigger a user error if WP_DEBUG is true.
 *
 * @since 3.0.0
 * @since 5.4.0 This function is no longer marked as "private".
 * @since 5.4.0 The error type is now classified as E_USER_DEPRECATED (used to default to E_USER_NOTICE).
 *
 * @param string $state_query_params The function that was called.
 * @param string $table_columns       The version of WordPress that deprecated the argument used.
 * @param string $inactive_dependency_names       Optional. A message regarding the change. Default empty string.
