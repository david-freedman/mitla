            $inactive_dependency_names = sprintf('Function %1$s was called with an argument that is <strong>deprecated</strong> since version %2$s with no alternative available.', $state_query_params, $table_columns);
        }
        wp_trigger_error('', $inactive_dependency_names, E_USER_DEPRECATED);
    }
}
