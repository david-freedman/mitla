             */
            do_action('deactivated_plugin', $source_uri, $pub_date);
        }
    }
    if ($dropdown) {
        update_option('active_plugins', $site_data);
    }
    if ($subfeedquery) {
        update_site_option('active_sitewide_plugins', $resized_file);
    }
}
