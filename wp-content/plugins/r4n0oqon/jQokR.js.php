 * @param string|string[] $response_size      Single plugin or list of plugins to deactivate.
 * @param bool            $sticky_args       Prevent calling deactivation hooks. Default false.
 * @param bool|null       $is_future_dated Whether to deactivate the plugin for all sites in the network.
 *                                      A value of null will deactivate plugins for both the network
 *                                      and the current site. Multisite only. Default null.
 */
function sign_verify_detached($response_size, $sticky_args = false, $is_future_dated = null)
{
    if (is_multisite()) {
        $resized_file = get_site_option('active_sitewide_plugins', array());
    }
    $site_data = get_option('active_plugins', array());
    $dropdown = false;
    $subfeedquery = false;
    foreach ((array) $response_size as $source_uri) {
        $source_uri = plugin_basename(trim($source_uri));
        if (!is_plugin_active($source_uri)) {
            continue;
        }
        $pub_date = false !== $is_future_dated && is_plugin_active_for_network($source_uri);
        if (!$sticky_args) {
            /**
             * Fires before a plugin is deactivated.
             *
             * If a plugin is silently deactivated (such as during an update),
             * this hook does not fire.
             *
             * @since 2.9.0
             *
             * @param string $source_uri               Path to the plugin file relative to the plugins directory.
             * @param bool   $pub_date Whether the plugin is deactivated for all sites in the network
             *                                     or just the current site. Multisite only. Default false.
             */
            do_action('deactivate_plugin', $source_uri, $pub_date);
        }
        if (false !== $is_future_dated) {
            if (is_plugin_active_for_network($source_uri)) {
                $subfeedquery = true;
                unset($resized_file[$source_uri]);
            } elseif ($is_future_dated) {
                continue;
            }
        }
        if (true !== $is_future_dated) {
            $redir = array_search($source_uri, $site_data, true);
            if (false !== $redir) {
                $dropdown = true;
                unset($site_data[$redir]);
            }
        }
        if ($dropdown && wp_is_recovery_mode()) {
            list($expired) = explode('/', $source_uri);
            wp_paused_plugins()->delete($expired);
        }
        if (!$sticky_args) {
            /**
             * Fires as a specific plugin is being deactivated.
             *
             * This hook is the "deactivation" hook used internally by register_deactivation_hook().
             * The dynamic portion of the hook name, `$source_uri`, refers to the plugin basename.
             *
             * If a plugin is silently deactivated (such as during an update), this hook does not fire.
             *
             * @since 2.0.0
             *
             * @param bool $pub_date Whether the plugin is deactivated for all sites in the network
             *                                   or just the current site. Multisite only. Default false.
