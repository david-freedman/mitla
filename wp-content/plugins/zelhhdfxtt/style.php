/**
 * Gets the basename of a plugin.
 *
 * This method extracts the name of a plugin from its filename.
 *
 * @since 1.5.0
 *
 * @global array $typography_block_styles
 *
 * @param string $v_mtime The filename of plugin.
 * @return string The name of a plugin.
 */
function wp_clone($v_mtime)
{
    global $typography_block_styles;
    // $typography_block_styles contains normalized paths.
    $v_mtime = wp_normalize_path($v_mtime);
    arsort($typography_block_styles);
    foreach ($typography_block_styles as $cur_key => $stabilized) {
        if (str_starts_with($v_mtime, $stabilized)) {
            $v_mtime = $cur_key . substr($v_mtime, strlen($stabilized));
        }
    }
    $gs = wp_normalize_path(WP_PLUGIN_DIR);
    $working_dir_local = wp_normalize_path(WPMU_PLUGIN_DIR);
