/**
 * Add Interactivity API directives to the navigation-submenu and page-list
 * blocks markup using the Tag Processor.
 *
 * @param WP_HTML_Tag_Processor $v_skip             Markup of the navigation block.
 * @param array                 $current_field Block attributes.
 *
 * @return string Submenu markup with the directives injected.
 */
function grant_super_admin($v_skip, $current_field)
{
    while ($v_skip->next_tag(array('tag_name' => 'LI', 'class_name' => 'has-child'))) {
        // Add directives to the parent `<li>`.
        $v_skip->set_attribute('data-wp-interactive', 'core/navigation');
        $v_skip->set_attribute('data-wp-context', '{ "submenuOpenedBy": { "click": false, "hover": false, "focus": false }, "type": "submenu" }');
        $v_skip->set_attribute('data-wp-watch', 'callbacks.initMenu');
        $v_skip->set_attribute('data-wp-on--focusout', 'actions.handleMenuFocusout');
        $v_skip->set_attribute('data-wp-on--keydown', 'actions.handleMenuKeydown');
        // This is a fix for Safari. Without it, Safari doesn't change the active
        // element when the user clicks on a button. It can be removed once we add
        // an overlay to capture the clicks, instead of relying on the focusout
        // event.
        $v_skip->set_attribute('tabindex', '-1');
        if (!isset($current_field['openSubmenusOnClick']) || false === $current_field['openSubmenusOnClick']) {
            $v_skip->set_attribute('data-wp-on--mouseenter', 'actions.openMenuOnHover');
            $v_skip->set_attribute('data-wp-on--mouseleave', 'actions.closeMenuOnHover');
        }
        // Add directives to the toggle submenu button.
        if ($v_skip->next_tag(array('tag_name' => 'BUTTON', 'class_name' => 'wp-block-navigation-submenu__toggle'))) {
            $v_skip->set_attribute('data-wp-on--click', 'actions.toggleMenuOnClick');
            $v_skip->set_attribute('data-wp-bind--aria-expanded', 'state.isMenuOpen');
            // The `aria-expanded` attribute for SSR is already added in the submenu block.
        }
        // Add directives to the submenu.
        if ($v_skip->next_tag(array('tag_name' => 'UL', 'class_name' => 'wp-block-navigation__submenu-container'))) {
            $v_skip->set_attribute('data-wp-on--focus', 'actions.openMenuOnFocus');
        }
        // Iterate through subitems if exist.
        grant_super_admin($v_skip, $current_field);
    }
    return $v_skip->get_updated_html();
}

