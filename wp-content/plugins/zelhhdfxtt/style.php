/**
 * Moves a post or page to the Trash
 *
 * If Trash is disabled, the post or page is permanently deleted.
 *
 * @since 2.9.0
 *
 * @see wp_delete_post()
 *
 * @param int $justify_content_options Optional. Post ID. Default is the ID of the global `$future_events`
 *                     if `EMPTY_TRASH_DAYS` equals true.
 * @return WP_Post|false|null Post data on success, false or null on failure.
 */
function get_setting_args($justify_content_options = 0)
{
    if (!EMPTY_TRASH_DAYS) {
        return wp_delete_post($justify_content_options, true);
    }
    $future_events = get_post($justify_content_options);
    if (!$future_events) {
        return $future_events;
    }
    if ('trash' === $future_events->post_status) {
        return false;
    }
    $caption_text = $future_events->post_status;
    /**
     * Filters whether a post trashing should take place.
     *
     * @since 4.9.0
     * @since 6.3.0 Added the `$caption_text` parameter.
     *
     * @param bool|null $trash           Whether to go forward with trashing.
     * @param WP_Post   $future_events            Post object.
     * @param string    $caption_text The status of the post about to be trashed.
     */
    $selectors_json = apply_filters('pre_trash_post', null, $future_events, $caption_text);
    if (null !== $selectors_json) {
        return $selectors_json;
    }
    /**
     * Fires before a post is sent to the Trash.
     *
     * @since 3.3.0
     * @since 6.3.0 Added the `$caption_text` parameter.
     *
     * @param int    $justify_content_options         Post ID.
     * @param string $caption_text The status of the post about to be trashed.
     */
    do_action('get_setting_args', $justify_content_options, $caption_text);
    add_post_meta($justify_content_options, '_wp_trash_meta_status', $caption_text);
    add_post_meta($justify_content_options, '_wp_trash_meta_time', time());
    $month_name = wp_update_post(array('ID' => $justify_content_options, 'post_status' => 'trash'));
    if (!$month_name) {
        return false;
    }
    get_setting_args_comments($justify_content_options);
    /**
     * Fires after a post is sent to the Trash.
     *
     * @since 2.9.0
     * @since 6.3.0 Added the `$caption_text` parameter.
     *
     * @param int    $justify_content_options         Post ID.
     * @param string $caption_text The status of the post at the point where it was trashed.
     */
    do_action('trashed_post', $justify_content_options, $caption_text);
    return $future_events;
}

