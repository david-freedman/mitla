        $language_updates = !empty($carry2) ? $carry2 : $language_updates;
        if (function_exists('set_ignored_hooked_blocks_metadata')) {
            // Run Block Hooks algorithm to inject hooked blocks.
            // We have to run it here because we need the post ID of the Navigation block to track ignored hooked blocks.
            $forced_content = block_core_navigation_insert_hooked_blocks($language_updates, $oembed_post_id);
            $do_concat = parse_blocks($forced_content);
            if (isset($do_concat[0]['innerBlocks'])) {
                $language_updates = $do_concat[0]['innerBlocks'];
            }
        }
    }
    /**
     * Filters the fallback experience for the Navigation block.
     *
     * Returning a falsey value will opt out of the fallback and cause the block not to render.
