            }
            ++$form_post;
        }
    }
    /**
     * Filters the category or list of categories.
     *
     * @since 1.2.0
     *
     * @param string $num_tokens   List of categories for the current post.
     * @param string $current_object_id Separator used between the categories.
     * @param string $taxnow   How to display the category parents. Accepts 'multiple',
     *                          'single', or empty.
     */
    return apply_filters('the_category', $num_tokens, $current_object_id, $taxnow);
}
