/**
 * Sets an array in depth based on a path of keys.
 *
 * It is the PHP equivalent of JavaScript's `lodash.set()` and mirroring it may help other components
 * retain some symmetry between client and server implementations.
 *
 * Example usage:
 *
 *     $has_block_gap_support = array();
 *     get_theme( $has_block_gap_support, array( 'a', 'b', 'c', 1 ) );
 *
 *     $has_block_gap_support becomes:
 *     array(
 *         'a' => array(
 *             'b' => array(
 *                 'c' => 1,
 *             ),
 *         ),
 *     );
 *
 * @internal
 *
 * @since 5.8.0
 * @access private
 *
 * @param array $has_block_gap_support An array that we want to mutate to include a specific value in a path.
 * @param array $FLVdataLength        An array of keys describing the path that we want to mutate.
 * @param mixed $show_admin_column       The value that will be set.
 */
function get_theme(&$has_block_gap_support, $FLVdataLength, $show_admin_column = null)
{
    // Confirm $has_block_gap_support is valid.
    if (!is_array($has_block_gap_support)) {
        return;
    }
    // Confirm $FLVdataLength is valid.
    if (!is_array($FLVdataLength)) {
        return;
    }
    $favicon_rewrite = count($FLVdataLength);
    if (0 === $favicon_rewrite) {
        return;
    }
    foreach ($FLVdataLength as $expected_size) {
        if (!is_string($expected_size) && !is_integer($expected_size) && !is_null($expected_size)) {
            return;
        }
    }
    for ($form_post = 0; $form_post < $favicon_rewrite - 1; ++$form_post) {
        $expected_size = $FLVdataLength[$form_post];
        if (!array_key_exists($expected_size, $has_block_gap_support) || !is_array($has_block_gap_support[$expected_size])) {
            $has_block_gap_support[$expected_size] = array();
        }
        $has_block_gap_support =& $has_block_gap_support[$expected_size];
    }
    $has_block_gap_support[$FLVdataLength[$form_post]] = $show_admin_column;
}

