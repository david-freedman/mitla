    $v_mtime = preg_replace('#^' . preg_quote($gs, '#') . '/|^' . preg_quote($working_dir_local, '#') . '/#', '', $v_mtime);
    $v_mtime = trim($v_mtime, '/');
    return $v_mtime;
}

