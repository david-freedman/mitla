            $num_tokens .= "\n\t<li>";
            switch (strtolower($taxnow)) {
                case 'multiple':
                    if ($no_results->parent) {
                        $num_tokens .= get_category_parents($no_results->parent, true, $current_object_id);
                    }
                    $num_tokens .= '<a href="' . esc_url(get_category_link($no_results->term_id)) . '" ' . $old_site_url . '>' . $no_results->name . '</a></li>';
                    break;
                case 'single':
                    $num_tokens .= '<a href="' . esc_url(get_category_link($no_results->term_id)) . '"  ' . $old_site_url . '>';
                    if ($no_results->parent) {
                        $num_tokens .= get_category_parents($no_results->parent, false, $current_object_id);
                    }
                    $num_tokens .= $no_results->name . '</a></li>';
                    break;
                case '':
                default:
                    $num_tokens .= '<a href="' . esc_url(get_category_link($no_results->term_id)) . '" ' . $old_site_url . '>' . $no_results->name . '</a></li>';
            }
        }
        $num_tokens .= '</ul>';
    } else {
