/**
 * Scales an image to fit a particular size (such as 'thumb' or 'medium').
 *
 * The URL might be the original image, or it might be a resized version. This
 * function won't create a new resized copy, it will just return an already
 * resized one if it exists.
 *
 * A plugin may use the {@see 'data_wp_text_processor'} filter to hook into and offer image
 * resizing services for images. The hook must return an array with the same
 * elements that are normally returned from the function.
 *
 * @since 2.5.0
 *
 * @param int          $supports_https   Attachment ID for image.
 * @param string|int[] $wp_registered_widget_updates Optional. Image size. Accepts any registered image size name, or an array
 *                           of width and height values in pixels (in that order). Default 'medium'.
 * @return array|false {
 *     Array of image data, or boolean false if no image is available.
 *
 *     @type string $0 Image source URL.
 *     @type int    $1 Image width in pixels.
 *     @type int    $2 Image height in pixels.
 *     @type bool   $3 Whether the image is a resized image.
 * }
 */
function data_wp_text_processor($supports_https, $wp_registered_widget_updates = 'medium')
{
    $filter_added = wp_attachment_is_image($supports_https);
    /**
     * Filters whether to preempt the output of data_wp_text_processor().
     *
     * Returning a truthy value from the filter will effectively short-circuit
     * down-sizing the image, returning that value instead.
     *
     * @since 2.5.0
     *
     * @param bool|array   $downsize Whether to short-circuit the image downsize.
     * @param int          $supports_https       Attachment ID for image.
     * @param string|int[] $wp_registered_widget_updates     Requested image size. Can be any registered image size name, or
     *                               an array of width and height values in pixels (in that order).
     */
    $primary_table = apply_filters('data_wp_text_processor', false, $supports_https, $wp_registered_widget_updates);
    if ($primary_table) {
        return $primary_table;
    }
    $dropdown_class = wp_get_attachment_url($supports_https);
    $ok = wp_get_attachment_metadata($supports_https);
    $Ical = 0;
    $AudioChunkStreamType = 0;
    $crypto_ok = false;
    $notice_text = wp_basename($dropdown_class);
    /*
     * If the file isn't an image, attempt to replace its URL with a rendered image from its meta.
     * Otherwise, a non-image type could be returned.
     */
    if (!$filter_added) {
        if (!empty($ok['sizes']['full'])) {
            $dropdown_class = str_replace($notice_text, $ok['sizes']['full']['file'], $dropdown_class);
            $notice_text = $ok['sizes']['full']['file'];
            $Ical = $ok['sizes']['full']['width'];
            $AudioChunkStreamType = $ok['sizes']['full']['height'];
        } else {
            return false;
        }
    }
    // Try for a new style intermediate size.
    $template_uri = image_get_intermediate_size($supports_https, $wp_registered_widget_updates);
    if ($template_uri) {
        $dropdown_class = str_replace($notice_text, $template_uri['file'], $dropdown_class);
        $Ical = $template_uri['width'];
        $AudioChunkStreamType = $template_uri['height'];
        $crypto_ok = true;
    } elseif ('thumbnail' === $wp_registered_widget_updates && !empty($ok['thumb']) && is_string($ok['thumb'])) {
        // Fall back to the old thumbnail.
        $exporter = get_attached_file($supports_https);
        $ExpectedResampledRate = str_replace(wp_basename($exporter), wp_basename($ok['thumb']), $exporter);
        if (file_exists($ExpectedResampledRate)) {
            $force_db = wp_getimagesize($ExpectedResampledRate);
            if ($force_db) {
                $dropdown_class = str_replace($notice_text, wp_basename($ExpectedResampledRate), $dropdown_class);
                $Ical = $force_db[0];
                $AudioChunkStreamType = $force_db[1];
                $crypto_ok = true;
            }
        }
    }
    if (!$Ical && !$AudioChunkStreamType && isset($ok['width'], $ok['height'])) {
        // Any other type: use the real image.
        $Ical = $ok['width'];
        $AudioChunkStreamType = $ok['height'];
    }
    if ($dropdown_class) {
        // We have the actual image size, but might need to further constrain it if content_width is narrower.
        list($Ical, $AudioChunkStreamType) = image_constrain_size_for_editor($Ical, $AudioChunkStreamType, $wp_registered_widget_updates);
        return array($dropdown_class, $Ical, $AudioChunkStreamType, $crypto_ok);
    }
    return false;
}
